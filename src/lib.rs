fn read_baseline(path: &Path) -> Result<Vec<report::BaselineWarning>> {
    let contents = std::fs::read_to_string(path)
        .with_context(|| format!("failed to read baseline: {}", path.display()))?;
    if let Ok(entries) = serde_json::from_str::<Vec<report::BaselineWarning>>(&contents) {
        return Ok(entries);
    }
    let ids = serde_json::from_str::<Vec<String>>(&contents)
        .with_context(|| format!("failed to parse baseline: {}", path.display()))?;
    Ok(ids
        .into_iter()
        .map(|id| report::BaselineWarning {
            id,
            message: None,
            location: None,
        })
        .collect())
}

fn write_output(path: &Path, text: &str) -> Result<()> {
    if let Some(parent) = path
        .parent()
        .filter(|parent| !parent.as_os_str().is_empty())
    {
        std::fs::create_dir_all(parent)
            .with_context(|| format!("failed to create output directory: {}", parent.display()))?;
    }
    std::fs::write(path, text)
        .with_context(|| format!("failed to write output: {}", path.display()))
}

pub fn batch_check(input: &Path) -> Result<RunOutput> {
    let contents = std::fs::read_to_string(input)
        .with_context(|| format!("failed to read batch input: {}", input.display()))?;
    let mut lines = Vec::new();
    let mut exit_code = 0;

    for entry in contents.lines().map(str::trim) {
        if entry.is_empty() || entry.starts_with('#') {
            continue;
        }
        let path = Path::new(entry);
        match check_repository(
            path,
            OutputFormat::Compact,
            Profile::Auto,
            Some(FailOn::Warn),
        ) {
            Ok(output) => {
                if output.exit_code != 0 {
                    exit_code = 1;
                }
                lines.push(output.text);
            }
            Err(error) => {
                exit_code = 1;
                lines.push(format!("repo-doctor path={entry} error={error}"));
            }
        }
    }

    Ok(RunOutput {
        text: lines.join("\n"),
        exit_code,
    })
}

