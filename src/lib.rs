    fn run_git(path: &Path, args: &[&str]) {
        let output = std::process::Command::new("git")
            .args(["-C"])
            .arg(path)
            .args(args)
            .output()
            .unwrap();
        assert!(
            output.status.success(),
            "git {:?} failed: {}",
            args,
            String::from_utf8_lossy(&output.stderr)
        );
    }

