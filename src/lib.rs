pub fn check_github_repository(repo: &str, format: OutputFormat) -> Result<RunOutput> {
    check_github_repository_with_options(repo, format, CheckOptions::default())
}

fn check_github_repository_with_options(
    repo: &str,
    format: OutputFormat,
    options: CheckOptions<'_>,
) -> Result<RunOutput> {
    let report = github::inspect(repo)?;
    let report = if options.warnings_only {
        report.warnings_only()
    } else {
        report
    };
    let text = match format {
        OutputFormat::Text => report.format_text(),
        OutputFormat::Json => serde_json::to_string_pretty(&report)?,
        OutputFormat::Markdown => report.format_markdown(),
        OutputFormat::Github => report.format_github_annotations(),
        OutputFormat::Sarif => report.format_sarif()?,
        OutputFormat::Compact => report.format_compact(),
        OutputFormat::Junit => report.format_junit(),
        OutputFormat::Html => report.format_html(),
    };

    Ok(RunOutput { text, exit_code: 0 })
}

pub fn list_profiles() -> String {
    Profile::catalog()
        .iter()
        .map(|(profile, hint)| format!("{profile}\t{hint}"))
        .collect::<Vec<_>>()
        .join("\n")
}

pub fn list_rules() -> String {
    report::known_rules()
        .iter()
        .map(|rule| format!("{}\t{}\t{}", rule.id, rule.severity, rule.description))
        .collect::<Vec<_>>()
        .join("\n")
}

pub fn explain_rule(rule_id: &str) -> Result<RunOutput> {
    let Some(rule) = report::known_rules().iter().find(|rule| rule.id == rule_id) else {
        bail!("unknown rule id: {rule_id}");
    };

    Ok(RunOutput {
        text: format!(
            "Rule: {}\nSeverity: {}\nPurpose: {}\nRemediation: Run `repo-doctor check --warnings-only` for concrete findings, or disable this rule in repo-doctor.toml with a rationale when it does not apply.",
            rule.id, rule.severity, rule.description
        ),
        exit_code: 0,
    })
}

pub fn validate_config_file(path: &Path) -> Result<RunOutput> {
    let findings = config::validate(path)?;
    if findings.is_empty() {
        Ok(RunOutput {
            text: format!("{} is valid", path.display()),
            exit_code: 0,
        })
    } else {
        Ok(RunOutput {
            text: findings.join("\n"),
            exit_code: 1,
        })
    }
}

pub fn suggest_next_steps(path: &Path) -> Result<RunOutput> {
    validate_repository_path(path)?;
    let config = config::load(path, None)?;
    let report = inspect_repository_with_config(path, Profile::Auto, &config);
    let warnings = report.warning_details();

    if warnings.is_empty() {
        return Ok(RunOutput {
            text: [
                "repo-doctor suggestion",
                "Status: no warnings found.",
                "Next steps:",
                "1. Add repo-doctor to CI:",
                "   repo-doctor ci --template generic > .github/workflows/repo-doctor.yml",
                "2. Keep the quality gate strict:",
                "   repo-doctor check --fail-on warn",
            ]
            .join("\n"),
            exit_code: 0,
        });
    }

    let mut lines = vec![
        "repo-doctor suggestion".to_owned(),
        format!("Status: {} warning(s) found.", warnings.len()),
        "Next steps:".to_owned(),
    ];

    for (index, warning) in warnings.iter().take(8).enumerate() {
        lines.push(format!(
            "{}. {}: {}",
            index + 1,
            warning.id,
            warning.message
        ));
        lines.push(format!("   {}", warning.remediation));
    }

    if warnings.len() > 8 {
        lines.push(format!(
            "... and {} more. Run `repo-doctor check --warnings-only` for the full list.",
            warnings.len() - 8
        ));
    }

    lines.push("Adopt gradually:".to_owned());
    lines.push("  repo-doctor baseline > repo-doctor-baseline.json".to_owned());
    lines
        .push("  repo-doctor check --baseline repo-doctor-baseline.json --fail-on warn".to_owned());

    Ok(RunOutput {
        text: lines.join("\n"),
        exit_code: 0,
    })
}

pub fn version_check(repo: &str) -> Result<RunOutput> {
    let current = env!("CARGO_PKG_VERSION");
    let latest = latest_release_tag(repo).unwrap_or_else(|error| format!("unavailable ({error})"));

    Ok(RunOutput {
        text: format!("repo-doctor current={current} latest={latest}"),
        exit_code: 0,
    })
}

fn latest_release_tag(repo: &str) -> Result<String> {
    let output = std::process::Command::new("gh")
        .args([
            "api",
            &format!("repos/{repo}/releases/latest"),
            "--jq",
            ".tag_name",
        ])
        .output()
        .context("failed to execute gh CLI")?;

    if !output.status.success() {
        bail!(
            "gh command failed: {}",
            String::from_utf8_lossy(&output.stderr).trim()
        );
    }

    Ok(String::from_utf8_lossy(&output.stdout).trim().to_owned())
}

pub fn create_baseline(path: &Path) -> Result<RunOutput> {
    validate_repository_path(path)?;
    let report = inspect_repository(path, Profile::Auto);
    let ids = report.warning_ids();

    Ok(RunOutput {
        text: serde_json::to_string_pretty(&ids)?,
        exit_code: 0,
    })
}

fn read_baseline(path: &Path) -> Result<Vec<String>> {
    let contents = std::fs::read_to_string(path)
        .with_context(|| format!("failed to read baseline: {}", path.display()))?;
    serde_json::from_str(&contents)
        .with_context(|| format!("failed to parse baseline: {}", path.display()))
}

pub fn batch_check(input: &Path) -> Result<RunOutput> {
    let contents = std::fs::read_to_string(input)
        .with_context(|| format!("failed to read batch input: {}", input.display()))?;
    let mut lines = Vec::new();
    let mut exit_code = 0;

    for entry in contents.lines().map(str::trim) {
        if entry.is_empty() || entry.starts_with('#') {
            continue;
        }
        let path = Path::new(entry);
        match check_repository(
            path,
            OutputFormat::Compact,
            Profile::Auto,
            Some(FailOn::Warn),
        ) {
            Ok(output) => {
                if output.exit_code != 0 {
                    exit_code = 1;
                }
                lines.push(output.text);
            }
            Err(error) => {
                exit_code = 1;
                lines.push(format!("repo-doctor path={entry} error={error}"));
            }
        }
    }

    Ok(RunOutput {
        text: lines.join("\n"),
        exit_code,
    })
}

pub fn github_setup(
    repo: &str,
    topics: &[String],
    homepage: Option<&str>,
    branch_protection: bool,
) -> Result<RunOutput> {
    let actions = github::setup(repo, topics, homepage, branch_protection)?;

    Ok(RunOutput {
        text: if actions.is_empty() {
            "No GitHub setup actions requested".to_owned()
        } else {
            actions.join("\n")
        },
        exit_code: 0,
    })
}

fn validate_repository_path(path: &Path) -> Result<()> {
    if !path.exists() {
        bail!("repository path does not exist: {}", path.display());
    }

    if !path.is_dir() {
        bail!("repository path is not a directory: {}", path.display());
    }

    Ok(())
}

pub fn completions(shell: Shell) -> Result<String> {
    let mut command = Cli::command();
    let name = command.get_name().to_owned();
    let mut buffer = Vec::new();

    clap_complete::generate(shell, &mut command, name, &mut buffer);

    Ok(String::from_utf8(buffer)?)
}

pub fn man_page() -> Result<String> {
    let command = Cli::command();
    let man = clap_mangen::Man::new(command);
    let mut buffer = Vec::new();

    man.render(&mut buffer)?;

    Ok(String::from_utf8(buffer)?)
}

